package cn.makese.model;

public class WorkInfo {
	private String sno;
	private String date;
	private String work;
	public String getDate() {
		return date;
	}
	public void setDate(String data) {
		this.date = data;
	}
	public String getSno() {
		return sno;
	}
	public void setSno(String sno) {
		this.sno = sno;
	}
	public String getWork() {
		return work;
	}
	public void setWork(String work) {
		this.work = work;
	}
	
	
}
